public class myApp {
    public static void main(String[] args) {
        // sum
        float a = 3;
        float b = 2;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("====================================");

        // sum
        float c = a + b;
        System.out.println("a + b = " + c);

        // subtraction
        c = a - b;
        System.out.println("a - b = " + c);

        // multiplication
        c = a * b;
        System.out.println("a * b = " + c);

        // division
        c = a / b;
        System.out.println("a / b = " + c);

        // mod
        c = a % b;
        System.out.println("a % b = " + c);

        System.out.println("======================================");

        // incremento
        a++;
        System.out.println("a++\na = " + a + "\n");

        // decremento
        b--;
        System.out.println("b--\nb = " + b);
    }
}
